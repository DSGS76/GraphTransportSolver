package com.io.graphtransportsolver.models.grafico.enums;

/**
 * Enum que representa los tipos de desigualdad en las restricciones.
 */
public enum TipoDesigualdad {
    /**
     * Restricción de tipo menor o igual (≤).
     */
    MENOR_IGUAL,

    /**
     * Restricción de tipo mayor o igual (≥).
     */
    MAYOR_IGUAL,

    /**
     * Restricción de tipo igual (=).
     */
    IGUAL
}
